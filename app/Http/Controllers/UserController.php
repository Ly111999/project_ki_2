    public function changeStatus()
    {
        $id = Input::get('id');
        $adminLevel = Input::get('adminLevel');
        $user = User::find($id);
        if ($user == null) {
            return view('errors.404-admin');
        }
        $user->adminLevel = $adminLevel;
        $user->save();
        return redirect('/admin/user');
    }

